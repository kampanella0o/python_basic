number = input("Day of the week? ")
if number == '1':
    print("Monday")
elif number == '2':
    print("Tuesday")
elif number == '3':
    print("Wednesday")
elif number == '4':
    print("Thursday")
elif number == '5':
    print("Friday")
elif number == '6':
    print("Saturday")
elif number == '7':
    print("Sunday")
else:
    print("There's no such day of the week")